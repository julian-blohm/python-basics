# equals operator ==
print(2 == 2)
print(2 == 4)
print("Hello" == "Hello")
print("Hello" == "Bye")
print(4 == 2*2)

# not equals operator !=
print(2 != 2)
print(2 != 4)
print("Hello" != "Hello")
print("Hello" != "Bye")
print(4 != 2*2)

# greater than
print(2 > 2)
print(2 > 4)
print(4 > 2)
apple_cost = 2
banana_cost = 3
print(apple_cost > banana_cost)

# greater than or equal to
print(2 >= 2)
print(2 >= 4)
print(4 >= 2)

# smaller than
print(2 < 2)
print(2 < 4)
print(4 < 2)
apple_cost = 2
banana_cost = 3
print(apple_cost < banana_cost)

# smaller than or equal to
print(2 <= 2)
print(2 <= 4)
print(4 <= 2)