# create variables
amount_of_apples = 2
cost_of_apple = 5


print(amount_of_apples * cost_of_apple)

# variables can be named using upper and lower case letters, digits and underscores
# name must begin with underscore or a letter _variable, variable
# upper and lower case variables are treated as different variables
# reserved words cannot be used too like if, for, True, False...
# change casing if you still want to use them "For" instead of "for"


cost_of_apple = cost_of_apple + 2 
print(cost_of_apple)

# or this way
cost_of_apple += 2
print(cost_of_apple)

# this operator can also be used with the other operators
# += ; -= ; *= ...

